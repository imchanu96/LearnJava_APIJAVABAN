package ezen.two;

public class MyMath {

//	반환타입 메서드명(매개변수, 매개변수)
//	return 문?
//	메서드에서 마지막으로 수행될 때 반환할 값이 있다면 사용한다.
	int add(int a, int b) {
		int result = 0;
		
		result = a + b;
		
		return result;
	}
	
	int sub(int a, int b) {
		int result = 0;
		
		result = a - b;
		
		return result;
	}
	
}
